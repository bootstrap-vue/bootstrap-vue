<template>
    <section>
        <table class="table">
            <thead>
            <tr>
                <template v-for="field in fields">
                    <th>{{field.label}}</th>
                </template>
            </tr>
            </thead>
            <tbody>
            <tr v-for="item in _items" :key="items_key">
                <td v-for="(field,key) in fields" :class="[field.class]">
                    <slot :name="key" :value="item[key]" :item="item">{{item[key]}}</slot>
                </td>
            </tr>
            </tbody>
        </table>

        <div class="justify-content-center row my-1" v-if="pagination">

            <b-pagination size="md"
                          :total-rows="items.length"
                          :per-page="perPage"
                          v-model="current"
            />

        </div>

    </section>
</template>

<script>
    import Pagination from './pagination.vue';

    export default{
        components: {bPagination: Pagination},

        data: () => {
            return {
                current: 1
            };
        },

        props: {
            items: {
                type: Array,
                default: () => []
            },
            fields: {
                type: Object,
                default: () => {}
            },
            pagination: {
                type: Boolean,
                default: false
            },
            perPage: {
                type: Number,
                default: 20
            },
            items_key: {
                type: String,
                default: null
            }
        },

        computed: {
            _items() {
                if (!this.items) {
                    return [];
                }
                return this.items.slice((this.current - 1) * this.perPage, this.current * this.perPage);
            }
        }

    };
</script>

